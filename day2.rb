def rps(input)
    total_points = 0
    shape_points = {'X':1,'Y':2,'Z':3}
    outcome_points = {'win':6,'draw':3,'lose':0}
    my_choice_hash = {'X':'Rock', 'Y':'Paper', 'Z':'Scissors'}
    opp_choice_hash = {'A':'Rock', 'B':'Paper', 'C':'Scissors'}
    winning_combos = {'Rock':'Scissors', 'Paper':'Rock', 'Scissors':'Paper'}

    puts input
    input.each_line do |line|
        choices = line.split
        opp_choice = choices[0].to_sym
        my_choice = choices[1].to_sym
        puts opp_choice_hash[opp_choice]
        puts my_choice_hash[my_choice]

        if my_choice_hash[my_choice] == opp_choice_hash[opp_choice]
            total_points += (shape_points[my_choice] + outcome_points[:draw])
        elsif winning_combos[my_choice_hash[my_choice].to_sym] == opp_choice_hash[opp_choice]
            total_points += (shape_points[my_choice] + outcome_points[:win])
        elsif winning_combos[opp_choice_hash[opp_choice].to_sym] == my_choice_hash[my_choice]
            total_points += (shape_points[my_choice] + outcome_points[:lose])
        end      
    end
    puts total_points
end


